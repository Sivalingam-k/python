#Iterate Over a list Using While Loop

ice_cream=['vannila','choco-chips','strawberry','dark choclate','pista']
print(len(ice_cream))
count=len(ice_cream)
i=0
while(i<count):
    print(ice_cream[i])
    i=i+1

